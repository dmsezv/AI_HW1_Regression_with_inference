from pydantic import BaseModel
from typing import List


class Car(BaseModel):
    name: str
    year: int
    km_driven: int
    fuel: str
    seller_type: str
    transmission: str
    owner: str
    mileage: str
    engine: str
    max_power: str
    torque: str
    seats: float


class Cars(BaseModel):
    list: List[Car]
